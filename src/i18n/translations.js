/**
 * Detect preferred language from browser (navigator.language / navigator.languages)
 * and country. Returns a supported lang code or defaultLang.
 * For India (IN): defaults to Hindi unless user has hi/ta/te in browser.
 */
export function getPreferredLang() {
  if (typeof navigator === 'undefined') return defaultLang
  const primary = (navigator.language || '').split('-')
  const langCode = primary[0]?.toLowerCase()
  const countryCode = primary[1]?.toUpperCase()

  if (countryCode === 'IN') {
    if (supportedLangs.includes(langCode) && ['hi', 'ta', 'te'].includes(langCode)) return langCode
    return 'hi'
  }

  const locales = navigator.languages && navigator.languages.length
    ? navigator.languages
    : [navigator.language]
  for (const locale of locales) {
    const code = (locale || '').split('-')[0].toLowerCase()
    if (supportedLangs.includes(code)) return code
  }
  return defaultLang
}

export function getTranslation(lang, keyPath) {
  const langData = translations[lang] ?? translations[defaultLang]
  const keys = keyPath.split('.')
  let value = langData
  for (const k of keys) {
    value = value?.[k]
  }
  return value ?? (translations[defaultLang] ? getTranslation(defaultLang, keyPath) : keyPath)
}
